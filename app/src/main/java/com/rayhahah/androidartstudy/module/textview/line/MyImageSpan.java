public class MyImageSpan implements LeadingMarginSpan.LeadingMarginSpan2 {

    private final BitmapDrawable mDrawable;
    private final int mWidth;
    private final int mHeight;
    private int mLines;
    private int mMargin;

    public MyImageSpan(BitmapDrawable drawable) {
        mDrawable = drawable;
        mWidth = mDrawable.copyBounds().width();
        mHeight = mDrawable.copyBounds().height();
        mMargin = mWidth;
    }

    @Override
    public int getLeadingMarginLineCount() {
        return mLines;
    }

    @Override
    public int getLeadingMargin(boolean first) {
        /**
         * 当前行数小于getLeadingMarginLineCount，first就是true
         */
        if (first) {
            return mMargin;
        } else {
            return 0;
        }
    }

    @Override
    public void drawLeadingMargin(Canvas c, Paint p, int x, int dir, int top, int baseline, int bottom,
                                  CharSequence text, int start, int end, boolean first, Layout layout) {
        if (top == 0) {
            mLines = (mHeight / (bottom - top)) + 1;
            Rect rect = new Rect(0, 0, mWidth, mHeight);
            Bitmap bitmap = mDrawable.getBitmap();
            /**
             * 位图是不可变的，所以copy一份可变的出来做调整
             */
            Bitmap zoomBitmap = CameraUtil.zoomBitmap(bitmap, mWidth, mHeight);
            Paint paint = new Paint(Paint.ANTI_ALIAS_FLAG);
            paint.setFilterBitmap(true);
            paint.setDither(true);
            c.drawBitmap(zoomBitmap, rect, rect, paint);
        }
    }
}
