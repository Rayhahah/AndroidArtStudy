public class BuilderMode {

    String name;

    private BuilderMode(Builder builder) {
        name = builder.name;
    }


    public static final class Builder {
        private String name;

        public Builder() {
        }

        public Builder setName(String val) {
            name = val;
            return this;
        }

        public BuilderMode build() {
            return new BuilderMode(this);
        }
    }
}
