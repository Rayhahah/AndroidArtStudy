public class VerticalImageSpan extends ImageSpan {

    private Drawable mDrawable;

    public VerticalImageSpan(Drawable d) {
        super(d);
        mDrawable = d;
    }

    /**
     * 获取段落所占的宽度值
     * @param paint
     * @param text
     * @param start
     * @param end
     * @param fm
     * @return
     */
    @Override
    public int getSize(Paint paint, CharSequence text, int start, int end, Paint.FontMetricsInt fm) {
        //ImageSpand的方法，获取传入的drawable
        Drawable drawable = getDrawable();
        if (drawable == null) {
            drawable = mDrawable;
        }
        Rect rect = drawable.getBounds();
        if (fm != null) {
            Paint.FontMetricsInt fontMetricsInt = paint.getFontMetricsInt();
            int fontHeight = fontMetricsInt.bottom - fontMetricsInt.top;
            int picHeight = rect.top - rect.bottom;

            int top = picHeight / 2 - fontHeight / 4;
            int bottom = picHeight / 2 + fontHeight / 4;

            fm.top = -bottom;
            fm.ascent = -bottom;
            fm.bottom = top;
            fm.descent = top;
        }
        return rect.right;
    }

    @Override
    public void draw(Canvas canvas, CharSequence text, int start, int end, float x, int top, int y, int bottom, Paint paint) {
        Drawable drawable = getDrawable();
        canvas.save();
        int transY = ((bottom - top) - drawable.getBounds().bottom) / 2 + top;
        canvas.translate(x, transY);
        drawable.draw(canvas);
        canvas.restore();
    }
}
