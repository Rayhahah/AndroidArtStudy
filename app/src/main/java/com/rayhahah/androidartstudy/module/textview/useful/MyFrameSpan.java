public class MyFrameSpan extends ReplacementSpan {

    private final Paint mPaint;
    private final int mStroke;
    private float mWidth;

    public MyFrameSpan(int color, int stroke) {
        mPaint = new Paint();
        mPaint.setColor(color);
        mPaint.setStrokeWidth(stroke);
        mStroke = stroke;
        mPaint.setStyle(Paint.Style.STROKE);
        mPaint.setDither(true);
        mPaint.setAntiAlias(true);
    }

    @Override
    public int getSize(@NonNull Paint paint, CharSequence text, int start, int end, @Nullable Paint.FontMetricsInt fm) {
        mWidth = paint.measureText(text, start, end) + mStroke * 2;
        return (int) mWidth;
    }

    @Override
    public void draw(@NonNull Canvas canvas, CharSequence text, int start, int end, float x, int top, int y, int bottom, @NonNull Paint paint) {
        canvas.drawRect(x + mStroke, top, x + mWidth - mStroke, bottom, mPaint);
        canvas.drawText(text, start, end, x, y, paint);
    }
}
